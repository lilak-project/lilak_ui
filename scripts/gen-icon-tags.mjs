/**
 * gen-icon-tags — regenerate src/icon-tags.js from Phosphor's own catalog.
 *
 *   npm run gen:icon-tags
 *
 * The picker searches by the SAME words phosphoricons.com does, which live in
 * @phosphor-icons/core's catalog (name + tags + categories). That package is a
 * devDependency and is NEVER imported at runtime: this script bakes the words for
 * the icons WE ship into a plain data module, so the bundle carries a few KB of
 * strings instead of the whole ~1500-icon catalog.
 *
 * Re-run it after adding icons to _EXTRA; CI-less repo, so the output is committed.
 */
import { icons } from '@phosphor-icons/core'
import { readFileSync, writeFileSync } from 'node:fs'
import { fileURLToPath } from 'node:url'
import { dirname, join } from 'node:path'

const here = dirname(fileURLToPath(import.meta.url))
const iconsJsx = join(here, '..', 'src', 'icons.jsx')
const out = join(here, '..', 'src', 'icon-tags.js')

const src = readFileSync(iconsJsx, 'utf8')
const kebab = (s) =>
  s.replace(/([a-z0-9])([A-Z])/g, '$1-$2').replace(/([A-Z]+)([A-Z][a-z])/g, '$1-$2').toLowerCase()

// The names we ship: the _EXTRA picker set + the curated PROJECT_ICONS.
const extra = src.match(/const _EXTRA = \{\n([\s\S]*?)\n\}\n/)
if (!extra) throw new Error('could not find the _EXTRA block in icons.jsx')
const fromExtra = extra[1].split(',').map((s) => s.trim()).filter(Boolean).map(kebab)

const proj = src.match(/export const PROJECT_ICONS = \[\n([\s\S]*?)\n\]/)
if (!proj) throw new Error('could not find PROJECT_ICONS in icons.jsx')
const fromProj = [...proj[1].matchAll(/'([^']+)'/g)].map((m) => m[1])

const want = [...new Set([...fromExtra, ...fromProj])].sort()
if (want.length < 100) throw new Error(`only found ${want.length} icons — the parse looks wrong`)

const byName = new Map(icons.map((i) => [i.name, i]))
const table = {}
const unknown = []
for (const name of want) {
  const meta = byName.get(name)
  if (!meta) { unknown.push(name); continue }
  // Phosphor marks recent icons with a "*new*"/"*updated*" tag — noise for search.
  const tags = (meta.tags || []).filter((t) => !/^\*.*\*$/.test(t))
  const words = [...new Set([...tags, ...(meta.categories || [])])]
  if (words.length) table[name] = words
}

const body = Object.entries(table)
  .map(([k, v]) => `  ${/^[a-z][a-z0-9]*$/.test(k) ? k : `'${k}'`}: ${JSON.stringify(v)},`)
  .join('\n')

writeFileSync(out, `/**
 * Icon search words — GENERATED by scripts/gen-icon-tags.mjs. Do not edit.
 *
 * name → the words phosphoricons.com matches that icon on (its tags + categories),
 * so searching the kit's picker finds an icon by what it IS ("nut" → acorn) and not
 * just by how it's spelled. Icons with no tags of their own are omitted; the picker
 * always matches on the name too.
 *
 * Regenerate with: npm run gen:icon-tags
 */
export const ICON_TAGS = {
${body}
}
`)
console.log(`wrote ${out}: ${Object.keys(table).length} icons tagged` +
  (unknown.length ? `, ${unknown.length} not in the catalog (${unknown.join(', ')})` : ''))
